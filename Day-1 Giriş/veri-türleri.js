/*primitive (ilkel) data-types*/

/* typeof değerin türünü sorgular ve konsola değerin türünü döndürür */
console.log(typeof "Yazı") //String
console.log(typeof 27) //Number
console.log(typeof true) //boolean
console.log(typeof null) //null(boş) /*null konsolda object olarak görünür fakat primitive içinde bulunur. bu yanlışın sebebi ise bir bugdan kaynaklanmaktadır*/
console.log(typeof undefined)//undefined(tanımsız)